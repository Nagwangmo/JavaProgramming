package day07_Operators;

public class RelationalOperator {

    public static void main(String[] args) {
        //relational operator  <,>,==(both numbers are equal), !=(not equal)

        int a = 10;
        int b = 20;
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}