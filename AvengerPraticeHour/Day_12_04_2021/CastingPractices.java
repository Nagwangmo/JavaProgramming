package Day_12_04_2021;

public class CastingPractices {

    public static void main(String[] args) {

        //byte to short
        byte b=10;
        short s=b; //Implicit casting

        //double to int

        double d=5.3;
        int i= (int) d; //explicit casting

        //long to int
        long l=999999999l;
        int k=(int)l;
        System.out.println("k = " + k);

        double f=12.12;



    }
}
